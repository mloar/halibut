wchar_t ps_glyph_to_unicode(char const *glyph)
{
    int i, j, k, c;

    i = -1;
    j = lenof(ps_glyphs_alphabetic);
    while (j-i > 1) {
	k = (i + j) / 2;
	c = strcmp(glyph, ps_glyphs_alphabetic[k]);

	if (c == 0)
	    return ps_codes_alphabetic[k];
	else if (c < 0)
	    j = k;
	else
	    i = k;
    }

    return 0xFFFF;		       /* illegal value means not found */
}

